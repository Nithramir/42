int		ft_sqrt(int nb)
{
	int div;
	int dd;

	dd = 1;
	if (nb < 0)
		return (0);
	while (dd < nb / dd)
	{
		dd++;
		if (dd * dd == nb)
			return (dd);
	}
	return (0);
}
