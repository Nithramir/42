int		ft_is_prime(int nb)
{
	int		div;
	int		dd;

	dd = 1;
	if (nb < 2)
		return (0);
	while (dd < nb / dd)
	{
		dd++;
		div = nb / dd;
		if (nb == dd * div)
			return (0);
	}
	return (1);
}
