#include "header.h"

void	ft_putnbr(int nb)
{
	if (nb == (-2147483648))
	{
		ft_putnbr(nb / 10);
		ft_putchar('8');
	}
	if (nb < 0 && nb != -2147483648)
	{
		ft_putchar('-');
		nb = (-1) * nb;
	}
	if ((nb / 10) != 0 && nb != -2147483648)
		ft_putnbr(nb / 10);
	if (nb != -2147483648)
		ft_putchar((nb % 10) + 48);
}
